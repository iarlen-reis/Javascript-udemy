/* 
Variáveis usando let, variável podem ser
modificadas ou serem apenas declaradas sem ser inicializadas.
Regras para criar:
    - Não podemos criar variáveis com palavras reservadas.
    - Variáveis precisam ter nomes significativos.
    - Não podemos começar um nome de variável com número.
    - Sempre iniciar com letra minuscula (camelCase).
*/

let nome; // Declarando a variável.

nome = 'João' // Inicializando a variável.

console.log(nome);

nome = 'iarlen'

console.log(nome);